    class TablePropiedadesModel implements TableModel{
        
        Propiedades prop=new Propiedades();     
        protected Object selected;
        private List lst=new ArrayList();
         
        TablePropiedadesModel(){
           this.setLst(prop.buscarTodasList());
        }
        
        public List getLst() {
            return lst;
        }

        public void setLst(List lst) {
            this.lst = lst;
        }

        public int getRowCount() {
            return this.getLst().size();
        }

        public int getColumnCount() {
            return 2;
        }

        public String getColumnName(int columnIndex) {
            switch (columnIndex){
                case 0: 
                    return "Nombre";
                case 1: 
                    return "Valor";
            }
            return "";
        }
        public Class<?> getColumnClass(int columnIndex) {
            return  getValueAt(0, columnIndex).getClass();
        }

        public boolean isCellEditable(int rowIndex, int columnIndex) {
            if(columnIndex==1){
                return true;
            }
            else{
                return false;
            }
        }
       

        public Object getValueAt(int rowIndex, int columnIndex) {
            switch(columnIndex){
                case 0:
                    return ((Propiedades)this.getLst().get(rowIndex)).getNombre();
                case 1:
                    return ((Propiedades)this.getLst().get(rowIndex)).getValor();                    
            }
            return null;
        }
        

        public void setValueAt(Object aValue, int rowIndex, int columnIndex) {
            
            Propiedades prop = (Propiedades)this.getLst().get(rowIndex);
            switch (columnIndex) {
                case 0: //Name
                    prop.setNombre((String)aValue);
                    break;
            case 1: //value
                    prop.setValor((String)aValue);
                    break;
           }
        }

        public void addTableModelListener(TableModelListener l) {
        }

        public void removeTableModelListener(TableModelListener l) {
        }

    }


