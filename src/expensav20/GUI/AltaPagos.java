    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.jCmb_Unidad.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar una unidad previamente");
            return;
        }
        Unidad uni = (Unidad)((ComboUnidadesModel)this.jCmb_Unidad.getModel()).getLst().get(this.jCmb_Unidad.getSelectedIndex());
        this.jTxt_Inquilino.setText("");
        if (uni.getInquilino()!=null){
            this.jTxt_Inquilino.setText(uni.getInquilino().getNombre());
        }
        if (uni.getPropietario()!=null){
            this.jTxt_Propietario.setText(uni.getPropietario().getNombre());
        }
        CtaCte cc=new CtaCte();
        cc.cargarCta(uni);
        Propiedades prop=new Propiedades();
        DecimalFormat nf=(DecimalFormat) DecimalFormat.getInstance(Locale.ENGLISH);
        nf.setGroupingUsed(false);
        nf.setRoundingMode(RoundingMode.HALF_UP);
        Map props=prop.buscarTodas();
        int cant_digitos= Integer.parseInt((String) props.get("cantidad_decimales_liquidacon"));
        nf.setMaximumFractionDigits(cant_digitos);
        this.jTxt_Monto.setText(nf.format(cc.getSaldo()));
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jBtn_DetalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_DetalleActionPerformed
        if (this.jCmb_Unidad.getSelectedIndex()==-1){
            return;
        }
        Unidad uni = (Unidad)((ComboUnidadesModel)this.jCmb_Unidad.getModel()).getLst().get(this.jCmb_Unidad.getSelectedIndex());
        if (uni!=null){
            ConsultaCuentaCorriente ctaCte=new ConsultaCuentaCorriente(uni);
            ctaCte.disableEliminar();
            ctaCte.setVisible(true);
        }
    }//GEN-LAST:event_jBtn_DetalleActionPerformed

    private void jBtn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_AceptarActionPerformed
        if (!validateFields())
            return;
        if (Double.valueOf(this.jTxt_Monto.getText()).compareTo(Double.valueOf(this.jFtf_Importe.getText()))<0){
            JOptionPane.showMessageDialog(this.getContentPane(),"El monto ingresado es superior al monto que se debe");
            return;
        }
        Unidad uni = (Unidad)((ComboUnidadesModel)this.jCmb_Unidad.getModel()).getLst().get(this.jCmb_Unidad.getSelectedIndex());
        /*if(existePago(uni)){
            JOptionPane.showMessageDialog(this.getContentPane(),"Ya fue cargado un pago a la unidad en la fecha");
            return;
        }
        */  
        Pago pago=new Pago();
        SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
        Concepto conc=(Concepto)((ComboConceptosModel)this.jCmb_Concepto.getModel()).getLst().get(this.jCmb_Concepto.getSelectedIndex());
        pago.setConcepto(conc);
        try {
            pago.setFecha(sdf.parse(this.jFtf_Fecha.getText()));
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        pago.setImporte(Double.valueOf(this.jFtf_Importe.getText()));        
        if (pago.generarPago(uni)){
            JOptionPane.showMessageDialog(this.getContentPane(),"El Pago fue guardado correctamente");
            this.jTxt_Monto.setText("");
            this.jFtf_Importe.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this.getContentPane(),"Ha ocurrido un problema, por favor intente nuevamente");
        }
    }//GEN-LAST:event_jBtn_AceptarActionPerformed

