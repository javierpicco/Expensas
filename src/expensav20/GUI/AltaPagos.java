   public boolean existePago(Unidad uni){
       boolean return_value=false;
       Calendar cal=Calendar.getInstance();
       Date today=cal.getTime();
       Pago pag=new Pago();
       List lst=pag.buscarSaldoxCriteria(uni,null,null,today,today);
       if (lst.size()>0){
           return_value=true;
       }
       return return_value;
   }
   
