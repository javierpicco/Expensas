    public void searchCombo(JComboBox jc){   
        String st=((JTextField)jc.getEditor().getEditorComponent()).getText();
        for(int i=0;i<jc.getModel().getSize();i++)
        {
           if(((String)jc.getItemAt(i)).contains(st)){
           jc.setSelectedIndex(i); 
           ((JTextField)jc.getEditor().getEditorComponent()).setText((String)jc.getSelectedItem());
           }
                  
        }
   }
    
   private boolean validateFields(){
 
        if (this.jFtf_Fecha.getText().length()==0){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar la fecha de pago");
            this.jFtf_Fecha.requestFocus();
            return false;
        }
        if(this.jFtf_Importe.getText().length()==0){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar el importe del pago");
            this.jFtf_Importe.requestFocus();
            return false;
        }
        if(this.jCmb_Concepto.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar el concepto");
            this.jCmb_Concepto.requestFocus();
            return false;
        }
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        df.setLenient(false);
        
        try {
            df.parse(this.jFtf_Fecha.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar una fecha válida");
            this.jFtf_Fecha.requestFocus();
            return false;  
        }
        try {
            Double.parseDouble(this.jFtf_Importe.getText());
        } catch(Exception e){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un importe válido");
            this.jFtf_Importe.requestFocus();
            return false;    
        }
        return true;
    }
