    class TableConsorciosModel implements TableModel{
        
        Consorcio cons= new Consorcio();
        protected Object selected;
        private List lst=new ArrayList();
         
        TableConsorciosModel(){
           
        }
        
        public List getLst() {
            return lst;
        }

        public void setLst(List lst) {
            this.lst = lst;
        }

        public int getRowCount() {
            return this.getLst().size();
        }

        public int getColumnCount() {
            return 4;
        }

        public String getColumnName(int columnIndex) {
            switch (columnIndex){
                case 0: 
                    return "Denominacion";
                case 1: 
                    return "Domicilio";
                case 2: 
                    return "Cuit";
                case 3: 
                    return "Fecha Baja";
            }
            return "";
        }
        public Class<?> getColumnClass(int columnIndex) {
            return this.getClass();
        }

        public boolean isCellEditable(int rowIndex, int columnIndex) {
            return false;
        }
       

        public Object getValueAt(int rowIndex, int columnIndex) {
            SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
            switch(columnIndex){
                case 0:
                    return ((Consorcio)this.getLst().get(rowIndex)).getDenominacion();
                case 1:
                    return ((Consorcio)this.getLst().get(rowIndex)).getDomcilio();
                case 2:
                    if (((Consorcio)this.getLst().get(rowIndex)).getCuit()==0){
                        return "";
                    }
                    else{
                        return ((Consorcio)this.getLst().get(rowIndex)).getCuit();
                    }    
                case 3:
                    if (((Consorcio)this.getLst().get(rowIndex)).getFecha_baja()==null){
                        return "";
                    }
                    else{
                        return sdf.format(((Consorcio)this.getLst().get(rowIndex)).getFecha_baja()).toString();
                    }
                    
            }
            
            return null;
        }
        

        public void setValueAt(Object aValue, int rowIndex, int columnIndex) {
        }

        public void addTableModelListener(TableModelListener l) {
        }

        public void removeTableModelListener(TableModelListener l) {
        }
    }
