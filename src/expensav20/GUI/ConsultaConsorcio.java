    private void jBtn_ActivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_ActivarActionPerformed
        if (this.jTlb_Resultados.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar un consorcio previamente");
            return;
        }
        Consorcio cons = (Consorcio)((TableConsorciosModel)this.jTlb_Resultados.getModel()).getLst().get(this.jTlb_Resultados.getSelectedRow());
        if (cons.getFecha_baja()==null){
            JOptionPane.showMessageDialog(this.getContentPane(),"El consorcio ya se encuentra activo");
            return;
        }
        if (cons.activar()!=0){
            JOptionPane.showMessageDialog(this.getContentPane(),"El consorcio fue activado correctamente");
            this.actualizar();
        }
        else{
            JOptionPane.showMessageDialog(this.getContentPane(),"Ocurrió un error al intentar activar el consorcio, intente nuevamente");
        }
    }//GEN-LAST:event_jBtn_ActivarActionPerformed

    private void jBtn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_EliminarActionPerformed
        if (this.jTlb_Resultados.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar un consorcio previamente");
            return;
        }
        Consorcio cons = (Consorcio)((TableConsorciosModel)this.jTlb_Resultados.getModel()).getLst().get(this.jTlb_Resultados.getSelectedRow());
        if (cons.getFecha_baja()!=null){
            JOptionPane.showMessageDialog(this.getContentPane(),"El consorcio ya fue dado de baja");
            return;
        }
        int i=JOptionPane.showConfirmDialog(this.getContentPane(),"Está seguro que desea eliminar el consorcio?");
        if(i==0){
            if (cons.eliminar()!=0){
                JOptionPane.showMessageDialog(this.getContentPane(),"El consorcio fue dado de baja correctamente");
                this.actualizar();
            }
            else{
                JOptionPane.showMessageDialog(this.getContentPane(),"Ocurrió un error al intentar dar de baja el consorcio, intente nuevamente");
            }
        }
    }//GEN-LAST:event_jBtn_EliminarActionPerformed

