    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.jCmb_Consorcio.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar un cosorcio previamente");
            return;
        }
        if (this.jFtf_Periodo.getText().length()==0){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un período previamente");
            return;
        }
        int consorcio_id=((Consorcio)((ComboConsorciosModel)this.jCmb_Consorcio.getModel()).getLst().get(this.jCmb_Consorcio.getSelectedIndex())).getCodigo();
        Map a = new HashMap();
        a.put("consorcio_id",String.valueOf(consorcio_id));
        SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
        Date periodo=null;
        try {
            periodo=sdf.parse("01/"+this.jFtf_Periodo.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un período del gasto válido. Ej. 01/2007 (Enero de 2007)");
            this.jFtf_Periodo.requestFocus();
            ex.printStackTrace();
            return;
        }
        sdf=new SimpleDateFormat("yyyy-MM-dd");
        a.put("periodo",sdf.format(periodo));
        if (this.jChk_Propietario.isSelected()){
            if (this.jTxt_Propietario.getText().length()==0){
                JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar un propietario previamente");
                return;
            }
            else{
                a.put("propietario_id",String.valueOf(this.getPropietario().getCodigo()));
                a.put("propietario_name",this.getPropietario().getNombre());
            }
        }
        else{
            a.put("propietario_id","0");
        }
        InformeExpensasComunes iec=new InformeExpensasComunes();
        iec.setParameters(a);
        iec.displayReport();
        
    }//GEN-LAST:event_jButton1ActionPerformed

