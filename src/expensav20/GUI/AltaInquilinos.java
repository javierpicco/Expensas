    private void jBtn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_AceptarActionPerformed
        if (this.validateFields()==false)
            return;
        Inquilino inq = new Inquilino();
        inq.setCp(this.jFTxt_CP.getText().trim());
        if (this.jFTxt_Cuit.getText().replace("-","").trim().length()>0){
            inq.setCuit(Long.parseLong(this.jFTxt_Cuit.getText().trim().replace("-","")));
        }
        if(this.jTxt_Domicilio.getText().length()>30){
            inq.setDomicilio(this.jTxt_Domicilio.getText().trim().substring(0,30));
        }
        else{
            inq.setDomicilio(this.jTxt_Domicilio.getText());
        }
            
        
        inq.setLocalidad(this.jTxt_Localidad.getText().trim());
        inq.setNombre(this.jTxt_Nombre.getText().trim());
        inq.setProvincia((Provincia)((ComboProvinciasModel)this.jCmb_Provincia.getModel()).getLst().get(this.jCmb_Provincia.getSelectedIndex()));
        inq.setUnidades(((TableUnidadesModel)this.jTlb_Unidades.getModel()).getLst());
        if (this.getInquilinoUpdated().getCodigo()==0){
            if (inq.validaExiste()>0){
                JOptionPane.showMessageDialog(this.getContentPane(),"Ya existe un inquilino con ese nombre. Cambie el nombre del inquilino o agregue las unidades al inquilino existente");                
                return;
            }
            inq.guardar();
            if (inq.getCodigo()!=0){
                JOptionPane.showMessageDialog(this.getContentPane(),"El Inquilino se guardó correctamente");
                this.limpiar();
            }
            else{
                JOptionPane.showMessageDialog(this.getContentPane(),"Ocurrió un problema, por favor intente nuevamente");
            }
        }
        else{
            inq.setCodigo(this.getInquilinoUpdated().getCodigo());
            if (inq.modificar()!=0){
                JOptionPane.showMessageDialog(this.getContentPane(),"El inquilino fue modificado correctamente");
                this.getCi().actualizar();
            }
            else{
                JOptionPane.showMessageDialog(this.getContentPane(),"Ocurrió un problema, por favor intente nuevamente");
            }
        }
               
    }//GEN-LAST:event_jBtn_AceptarActionPerformed

    private void jBtn_QuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_QuitarActionPerformed
        if (this.jTlb_Unidades.getRowCount()!=0){
            ((TableUnidadesModel)this.jTlb_Unidades.getModel()).getLst().remove(this.jTlb_Unidades.getSelectedRow());
            this.jTlb_Unidades.updateUI();
            if (this.jTlb_Unidades.getRowCount()!=0){
                this.jTlb_Unidades.setRowSelectionInterval(0,0);
            }
         }

    }//GEN-LAST:event_jBtn_QuitarActionPerformed

