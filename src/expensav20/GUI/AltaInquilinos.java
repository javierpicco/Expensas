    public void searchCombo(JComboBox jc){   
        String st=((JTextField)jc.getEditor().getEditorComponent()).getText();
        for(int i=0;i<jc.getModel().getSize();i++)
        {
           if(((String)jc.getItemAt(i)).contains(st)){
           jc.setSelectedIndex(i); 
           ((JTextField)jc.getEditor().getEditorComponent()).setText((String)jc.getSelectedItem());
           }
                  
        }
   }
   
   public void limpiar(){
       this.jTxt_Domicilio.setText("");
       this.jTxt_Localidad.setText("");
       this.jTxt_Nombre.setText("");
       this.jCmb_Consorcio.setSelectedIndex(-1);
       this.jCmb_Provincia.setSelectedIndex(-1);
       this.jCmb_Unidad.setSelectedIndex(-1);
       ((ComboUnidadesModel)this.jCmb_Unidad.getModel()).getLst().clear();
       this.jCmb_Unidad.updateUI();
       ((TableUnidadesModel)this.jTlb_Unidades.getModel()).getLst().clear();
       this.jTlb_Unidades.updateUI();
       this.jFTxt_CP.setText("");
       this.jFTxt_Cuit.setText("");
   }
   
   
    private boolean validateFields(){ 

        if(this.jTxt_Nombre.getText().trim().length()==0 || this.jTxt_Nombre.getText().trim().length()>35){
            JOptionPane.showMessageDialog(this.getContentPane(),"El nombre no puede estar vacío o contener mas de 35 caracteres");
            this.jTxt_Nombre.requestFocus();
            return false;
        }

        if (this.jFTxt_Cuit.getText().replace("-","").trim().length()<11 && this.jFTxt_Cuit.getText().replace("-","").trim().length()>0){
          JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un CUIT válido");
          this.jFTxt_Cuit.requestFocus();
          return false;
        }

        
        if (this.jTxt_Domicilio.getText().trim().length()==0){
          JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un domicilio");
          this.jTxt_Domicilio.requestFocus();
          return false;
        }
        

        
        if(this.jTxt_Localidad.getText().trim().length()==0 || this.jTxt_Localidad.getText().trim().length()>30){
            JOptionPane.showMessageDialog(this.getContentPane(),"La localidad no puede estar vacío o contener mas de 30 caracteres");
            this.jTxt_Localidad.requestFocus();
            return false;
        }
        
        if (this.jFTxt_CP.getText().trim().length()==0){
          JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un código postal válido. 4 Dígitos");
          this.jFTxt_CP.requestFocus();
          return false;
        }
        
        if (this.jTlb_Unidades.getRowCount()==0){
          JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar al menos una propiedad para el propietario");
          this.jCmb_Consorcio.requestFocus();
          return false;
        }
        
        return true;
    }
    public int buscarProvinciaCombo(Provincia prov2)
    {
        List lst=((ComboProvinciasModel)this.jCmb_Provincia.getModel()).getLst();
        Iterator it=lst.iterator();
        int index=0;
        while (it.hasNext()){
            Provincia prov = (Provincia)it.next();
            if (prov.getCodigo()==prov2.getCodigo()){
                return index;
            }
            index++;
        }
        return -1;
            
    }

