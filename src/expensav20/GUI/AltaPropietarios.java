    private void jBtn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_AceptarActionPerformed
        if (this.validateFields()==false)
            return;
        Propietario prop = new Propietario();
        prop.setCp(this.jFTxt_CP.getText());
        if (this.jFTxt_Cuit.getText().replace("-","").trim().length()!=0){
            prop.setCuit(Long.parseLong(this.jFTxt_Cuit.getText().replace("-","")));
        }
        if(this.jTxt_Domicilio.getText().length()>30){
            prop.setDomicilio(this.jTxt_Domicilio.getText().substring(0,30));
        }
        else{
            prop.setDomicilio(this.jTxt_Domicilio.getText());
        }
        prop.setLocalidad(this.jTxt_Localidad.getText());
        prop.setNombre(this.jTxt_Nombre.getText());
        prop.setProvincia((Provincia)((ComboProvinciasModel)this.jCmb_Provincia.getModel()).getLst().get(this.jCmb_Provincia.getSelectedIndex()));
        prop.setTelefono(this.jTxt_Telefono.getText());
        prop.setUnidades(((TableUnidadesModel)this.jTbl_Unidades.getModel()).getLst());
        if (this.getPropietarioUpdated().getCodigo()==0){
            if (prop.validaExiste()>0){
                JOptionPane.showMessageDialog(this.getContentPane(),"Ya existe un propietario con ese nombre. Cambie el nombre del propietario o agregue las uniades al propietario existente");                
                return;
            }
            prop.guardar();
            if (prop.getCodigo()!=0){
                JOptionPane.showMessageDialog(null,"El propietario fue dado de alta correctamente");
                this.limpiar();
            }
            else{
                JOptionPane.showMessageDialog(null,"Ocurrió un problema, intente nuevamente");
            }
        }
        else{
            prop.setCodigo(this.getPropietarioUpdated().getCodigo());
            if(prop.modificar()!=0){
                JOptionPane.showMessageDialog(this.getContentPane(),"El propietario fue modificado correctamente");
                this.getCp().actualizar();
            }
            else{
                JOptionPane.showMessageDialog(this.getContentPane(),"Ocurrió un problema al intentar modificar el propietario");
            }
        }
        
    }//GEN-LAST:event_jBtn_AceptarActionPerformed

