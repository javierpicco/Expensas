    public int buscarProvinciaCombo(Provincia prov2)
    {
        List lst=((ComboProvinciasModel)this.jCmb_Provincia.getModel()).getLst();
        Iterator it=lst.iterator();
        int index=0;
        while (it.hasNext()){
            Provincia prov = (Provincia)it.next();
            if (prov.getCodigo()==prov2.getCodigo()){
                return index;
            }
            index++;
        }
        return -1;
            
    }

