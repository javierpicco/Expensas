    private void jBtn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_AceptarActionPerformed
        if (this.validateFields()==false)
            return;
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        GastoImputado gi=new GastoImputado();
        gi.setComprobante(this.jTxt_Comprobante.getText());
        gi.setConcepto((Concepto)((ComboConceptosModel)this.jCmb_Gasto.getModel()).getLst().get(this.jCmb_Gasto.getSelectedIndex()));
        gi.setConsorcio((Consorcio)((ComboConsorciosModel)this.jCmb_Consorcio.getModel()).getLst().get(this.jCmb_Consorcio.getSelectedIndex()));
        gi.setDescripcion(this.jTxt_Descripcion.getText());
        try {
            gi.setFecha(df.parse(this.jFtf_Fecha.getText()));
            gi.setPeriodo(df.parse("01/"+this.jFtf_Periodo.getText()));
            gi.setPeriodoGasto(df.parse("01/"+this.jFtf_PeriodoGasto.getText()));
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        gi.setImporte(Double.valueOf(this.jFtf_Importe.getText()));
       
        if (this.jRdb_Unidad.isSelected()==true){
            gi.setUnidad((Unidad)((ComboUnidadesModel)this.jCmb_Unidad.getModel()).getLst().get(this.jCmb_Unidad.getSelectedIndex()));
        }        
        gi.guardar();
        if (gi.getCodigo()!=0){
            JOptionPane.showMessageDialog(this.getContentPane(),"El gasto se guardó correctamente");
            this.limpiar(false);
            this.jCmb_Unidad.requestFocus();
        }
    }//GEN-LAST:event_jBtn_AceptarActionPerformed

