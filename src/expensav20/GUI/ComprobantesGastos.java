    private void jBtn_ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_ConsultarActionPerformed

        if (this.jCmb_Consorcio.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar un Consorcio");
            this.jCmb_Consorcio.requestFocus();
            return;
        }
        if (this.jRdb_Unidad.isSelected()){
            if (this.jCmb_Unidad.getSelectedIndex()==-1){
                JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar una unidad");
                this.jCmb_Unidad.requestFocus();
                return;
            }
            Unidad uni =(Unidad) ((ComboUnidadesModel)this.jCmb_Unidad.getModel()).getLst().get(this.jCmb_Unidad.getSelectedIndex());
            this.jTxt_Consorcio.setText(uni.getConsorcio().getDenominacion());
            this.jTxt_Propietario.setText(uni.getPropietario().getNombre());
            this.jTxt_Unidad.setText(uni.getDescripcion());
        }
        else{
            Consorcio con = (Consorcio)((ComboConsorciosModel)this.jCmb_Consorcio.getModel()).getLst().get(this.jCmb_Consorcio.getSelectedIndex());
            this.jTxt_Consorcio.setText(con.getDenominacion());
        }
    }//GEN-LAST:event_jBtn_ConsultarActionPerformed

