    public void searchCombo(JComboBox jc){   
        String st=((JTextField)jc.getEditor().getEditorComponent()).getText();
        for(int i=0;i<jc.getModel().getSize();i++)
        {
           if(((String)jc.getItemAt(i)).contains(st)){
           jc.setSelectedIndex(i); 
           ((JTextField)jc.getEditor().getEditorComponent()).setText((String)jc.getSelectedItem());
           }
                  
        }
   }
    //Método para validar Empty Fields
    private boolean validateFields(){
 
        if (this.jTxt_Consorcio.getText().trim().length()==0){
          JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar y cargar un Consorcio");
          this.jCmb_Consorcio.requestFocus();
          return false;  
        }
        if (this.jCmb_Gasto.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar un gasto para ser cargado");
            this.jCmb_Gasto.requestFocus();
            return false;
        }
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        df.setLenient(false);
        
        try {
            df.parse(this.jFtf_Fecha.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar una fecha válida");
            this.jFtf_Fecha.requestFocus();
            return false;  
        }
        try {
            df.parse("01/"+this.jFtf_Periodo.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un período válido. Ej. 01/2007 (Enero de 2007)");
            this.jFtf_Periodo.requestFocus();
            return false;  
        }
        try {
            df.parse("01/"+this.jFtf_PeriodoGasto.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un período del gasto válido. Ej. 01/2007 (Enero de 2007)");
            this.jFtf_PeriodoGasto.requestFocus();
            return false;  
        }
        if(this.jTxt_Descripcion.getText().trim().length()>40){
            JOptionPane.showMessageDialog(this.getContentPane(),"La descripción no pude ser mayor a 40 caracteres");
            this.jTxt_Descripcion.requestFocus();
            return false;
        }
        try {
            Double.parseDouble(this.jFtf_Importe.getText());
        } catch(Exception e){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un importe válido");
            this.jFtf_Importe.requestFocus();
            return false;    
        }
        if(this.jTxt_Comprobante.getText().trim().length()>30){
            JOptionPane.showMessageDialog(this.getContentPane(),"El número de comprobante no pude ser mayor a 30 caracteres");
            this.jTxt_Comprobante.requestFocus();
            return false;
        }
        
        return true;
    }
    public void limpiar(boolean todo){
        this.jTxt_Descripcion.setText("");
        this.jFtf_Importe.setText("");
        this.jTxt_Comprobante.setText("");
        if (todo==true){
            this.jRdb_Unidad.setSelected(false);
            this.jRdb_consorcio.setSelected(false);
            this.jCmb_Consorcio.setSelectedIndex(-1);
            this.jCmb_Gasto.setSelectedIndex(-1);
            this.jCmb_Unidad.setSelectedIndex(-1);
            this.jTxt_Consorcio.setText("");
            this.jTxt_Propietario.setText("");
            this.jTxt_Unidad.setText("");
            this.jFtf_Periodo.setText("");
        }
    }
