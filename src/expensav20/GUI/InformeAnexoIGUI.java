    public void setearPropietario(String nombre){
        this.jTxt_Propietario.setText(nombre);
        Unidad unidad=new Unidad();
        List lst=unidad.buscarxPropietario(this.getPropietario());
        Iterator it=lst.iterator();
        StringBuffer sb=new StringBuffer();
        while (it.hasNext()){
            Unidad uni = (Unidad)it.next();
            sb.append(uni.getDescripcion());
            sb.append("-");
        }
        this.jTxt_Unidades.setText(sb.toString());
    }
       private boolean validateFields(){
 
        if (this.jFtf_Periodo.getText().length()==0){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar el período");
            this.jFtf_Periodo.requestFocus();
            return false;
        }
        if (this.jTxt_Propietario.getText().length()==0){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar un propietario");
            return false;
        }
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        df.setLenient(false);
        
        try {
            df.parse("01/"+this.jFtf_Periodo.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un período válido");
            this.jFtf_Periodo.requestFocus();
            return false;  
        }
        return true;
    }

