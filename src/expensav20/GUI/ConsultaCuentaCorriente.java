    private void jBtn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_EliminarActionPerformed
        if (this.jTlb_CtaCte.getRowCount()==0){
            JOptionPane.showMessageDialog(this.getContentPane(),"No existen filas para eliminar");
            return;            
        }

        if (Double.valueOf(this.jTxt_Saldo.getText())!=0){
            JOptionPane.showMessageDialog(this.getContentPane(),"Sólo puede eliminar períodos de cuenta corriente que tengan saldo 0");
            return;
        }
        
        if (JOptionPane.showConfirmDialog(this.getContentPane(),"Está seguro que desea eliminar este período de cta. cte?")==JOptionPane.YES_OPTION){
            List ctacte=((TableCtaCteModel)this.jTlb_CtaCte.getModel()).getLst();
            Iterator ctait=ctacte.iterator();
            while (ctait.hasNext()){
                CtaCteItem cci=(CtaCteItem)ctait.next();
                if (cci instanceof Pago){
                    ((Pago)cci).eliminar();
                }
                else{
                    ((DetalleLiquidacion)cci).eliminar();
                }
            }
            JOptionPane.showMessageDialog(this.getContentPane(),"El período de cuenta corriente fue eliminado correctamente");                
            Inquilino inq=null;
            Propietario prop=null;
            Unidad uni=null;
            Date fechaDesde=null;
            Date fechaHasta=null;
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            if (this.getInquilino()!=null){
                inq=this.getInquilino();
            }
            if (this.getPropietario()!=null){
                prop=this.getPropietario();
            }
            if(this.jChk_Unidad.isSelected()){
                if(this.jCmb_Unidad.getSelectedIndex()!=-1){
                    uni = (Unidad)((ComboUnidadesModel)this.jCmb_Unidad.getModel()).getLst().get(this.jCmb_Unidad.getSelectedIndex());        
                }
            }
            if(this.jChk_Periodo.isSelected()){
                try {
                    df.setLenient(false);
                    fechaDesde=df.parse(this.jFtf_FechaDesde.getText());
                    fechaHasta=df.parse(this.jFtf_FechaHasta.getText());
                } catch (ParseException ex) {
                    JOptionPane.showMessageDialog(this.getContentPane(),"La fecha desde o fecha hasta ingresada no es una fecha válida");
                    return;
                }
                
            }
            CtaCte ctaCte=new CtaCte();        
            ctaCte.cargarCta(uni,prop,inq,fechaDesde,fechaHasta);
            ((TableCtaCteModel)this.jTlb_CtaCte.getModel()).setCc(ctaCte);
            ((TableCtaCteModel)this.jTlb_CtaCte.getModel()).setLst(ctaCte.getItems());
            this.jTlb_CtaCte.updateUI();
            this.jTxt_Saldo.setText(ctaCte.getSaldo().toString());  
        }
    }//GEN-LAST:event_jBtn_EliminarActionPerformed

    private void jBtn_ListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_ListarActionPerformed
        String filtro=new String();
        
        if (this.jChk_Unidad.isSelected()){
            Unidad uni=(Unidad)((ComboUnidadesModel)this.jCmb_Unidad.getModel()).getLst().get(this.jCmb_Unidad.getSelectedIndex());
            filtro=filtro+="Unidad: " + uni.getDescripcion() + " del consorcio " + uni.getConsorcio().getDenominacion() + "\n";
        }
        if (this.jChk_Propietario.isSelected()){
            filtro=filtro+="Propietario: " + this.jTxt_Propietario.getText()+"\n";
        }
        if (this.jChk_Inquilino.isSelected()){
            filtro=filtro+="Inquilion: " + this.jTxt_Inquilino.getText() + "\n";
        }
        if (this.jChk_Periodo.isSelected()){
            SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
            try {
                filtro=filtro+"Entre el dìa: " + sdf.format(sdf.parse(this.jFtf_FechaDesde.getText())) + " y el día: " + sdf.format(sdf.parse(this.jFtf_FechaHasta.getText())) + "\n";
            } catch (ParseException ex) {
                ex.printStackTrace();
            }
        }
        Map a = new HashMap();
        a.put("filtro",filtro);
        InformeCtaCte icc=new InformeCtaCte();
        icc.setParameters(a);
        icc.displayReport(this.jTlb_CtaCte);
        
    }//GEN-LAST:event_jBtn_ListarActionPerformed

