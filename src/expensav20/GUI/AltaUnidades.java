    private void jBtn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_AceptarActionPerformed
        if (this.validateFields()==false)
            return;
        Unidad uni=new Unidad();
        Consorcio cons=new Consorcio();
        cons=(Consorcio)((ComboConsorciosModel)this.jCmb_Consorcio.getModel()).getLst().get(this.jCmb_Consorcio.getSelectedIndex());
        uni.setDescripcion(this.jTxt_descripcion.getText());
        uni.setConsorcio(cons);
        if (this.getUnidadUpdated().getCodigo()==0)
        {
            uni.guardar();
            if (uni.getCodigo()!=0){
                JOptionPane.showMessageDialog(null,"La unidad se guardó correctamente");
                this.limpiar();
            }
            else{
                JOptionPane.showMessageDialog(null,"Ocurrió un problema, intente nuevamente por favor");
            }
        }
        else{
            uni.setCodigo(this.getUnidadUpdated().getCodigo());
            if (uni.modificar()!=0){
                JOptionPane.showMessageDialog(this.getContentPane(),"La unidad se modificó correctamente");
                this.getCu().actualizar();
          
            }
            else{
                JOptionPane.showMessageDialog(this.getContentPane(),"Ocurrió un problema al modificar la unidad, por favor intente nuevamente");
            }
        }
    }//GEN-LAST:event_jBtn_AceptarActionPerformed

