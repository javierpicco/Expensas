    public int buscarUnidadCombo(Unidad uni)
    {
        List lst=((ComboConsorciosModel)this.jCmb_Consorcio.getModel()).getLst();
        Iterator it=lst.iterator();
        int index=0;
        while (it.hasNext()){
            Consorcio con = (Consorcio)it.next();
            if (con.getCodigo()==uni.getConsorcio().getCodigo()){
                return index;
            }
            index++;
        }
        return -1;
            
    }

