    private boolean validateFields()
    {
        
     
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        df.setLenient(false);
        
        try {
            df.parse("01/"+this.jFtf_Periodo.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un período válido. Ej. 01/2007 (Enero de 2007)");
            this.jFtf_Periodo.requestFocus();
            return false;  
        }
        
        if(this.jTxt_Descripcion.getText().trim().length()>40){
            JOptionPane.showMessageDialog(this.getContentPane(),"La descripción no pude ser mayor a 40 caracteres");
            this.jTxt_Descripcion.requestFocus();
            return false;
        }
        
        try {
            Double.parseDouble(this.jTxt_Monto.getText());
        } catch(Exception e){
            JOptionPane.showMessageDialog(this.getContentPane(),"Debe ingresar un importe válido");
            this.jTxt_Monto.requestFocus();
            return false;    
        }
        
        if (this.jCmb_Consorcio_Hasta.getSelectedIndex()==-1){
          JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar un Consorcio al cual transferir el gasto");
          this.jCmb_Consorcio_Hasta.requestFocus();
          return false;  
        }
        if (!this.getGiUpdated().getConcepto().getCoeficiente().isDistribuible()){
            if (this.jCmb_Unidad_Hasta.getSelectedIndex()==-1){
                JOptionPane.showMessageDialog(this.getContentPane(),"Debe seleccionar una Unidad a la cual transferir el gasto");
                this.jCmb_Unidad_Hasta.requestFocus();
                return false;  
            }
        }
        
        return true;
    }

