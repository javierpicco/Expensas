    private void jBtn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_AceptarActionPerformed
        if (!this.validateFields())
            return;
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        GastoImputado gi=new GastoImputado();
        gi.setConsorcio((Consorcio)((ComboConsorciosHastaModel)this.jCmb_Consorcio_Hasta.getModel()).getLst().get(this.jCmb_Consorcio_Hasta.getSelectedIndex()));
        gi.setDescripcion(this.jTxt_Descripcion.getText());
        try {
            gi.setPeriodo(df.parse("01/"+this.jFtf_Periodo.getText()));
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        gi.setImporte(Double.valueOf(this.jTxt_Monto.getText()));
        gi.setCodigo(this.getGiUpdated().getCodigo());
        gi.setPeriodoGasto(this.getGiUpdated().getPeriodoGasto());
        if (!this.getGiUpdated().getConcepto().getCoeficiente().isDistribuible()){
            gi.setUnidad((Unidad)((ComboUnidadesHastaModel)this.jCmb_Unidad_Hasta.getModel()).getLst().get(this.jCmb_Unidad_Hasta.getSelectedIndex()));
        }        
        if (gi.transferir()!=0){
            JOptionPane.showMessageDialog(this.getContentPane(),"El gasto se ha transferido correctamente");
            this.getCci().actualizar();
        }
        else{
            JOptionPane.showMessageDialog(this.getContentPane(),"Ocurrió un problema, por favor intente nuevamente");
        }
            

        
    }//GEN-LAST:event_jBtn_AceptarActionPerformed

